import type { StackPoint } from '@/core/models/Peak';

export interface SliceDefinition {
  id: string;
  label: string;
  description: string;
  points: StackPoint[];
  peakIndex: number;
}

// Helper: build 5 StackPoints with even x spacing
function makeSlice(
  id: string,
  label: string,
  description: string,
  heights: [number, number, number, number, number],
  bottoms: [number, number, number, number, number],
  peakIndex = 2,
): SliceDefinition {
  const xStep = 80;
  return {
    id,
    label,
    description,
    peakIndex,
    points: heights.map((h, i) => ({ x: i * xStep, y: h, y0: bottoms[i] })),
  };
}

